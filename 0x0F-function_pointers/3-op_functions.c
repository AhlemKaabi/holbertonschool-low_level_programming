#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - add 2 numbers
 * @a: param1
 * @b: param2
 * Return: int
 **/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 numbers
 * @a: param1
 * @b: param2
 * Return: int
 **/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 numbers
 * @a: param1
 * @b: param2
 * Return: int
 **/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 numbers
 * @a: param1
 * @b: param2
 * Return: int
 **/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo 2 numbers
 * @a: param1
 * @b: param2
 * Return: int
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
